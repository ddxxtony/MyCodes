    private void setFilas() throws SQLException {
        try {
            String sql = "Select nombre,apellidos,tipoUsuario from usuario";
                objConn.Consult(sql);
                
                Object datos[] = new Object[4];
                
                while (!objConn.rs.isAfterLast()) {
                    for (int i = 0; i < 3 ;i++) {
                        datos[i] = objConn.rs.getString(i+1);
                    }
                    modeloTabla.addRow(datos);
                    objConn.rs.next();
                }
                objConn.desConnect();
        } catch (SQLException ex) {}
    }
    
